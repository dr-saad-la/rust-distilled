/// Calculates the hyperbolic arcsine of `a` and returns the result.
///
/// # Arguments
///
/// * `a` - The value to calculate the hyperbolic arcsine for.
///
/// # Returns
///
/// The hyperbolic arcsine of `a`.
///
/// # Examples
///
/// ```
/// use advanced_calculator::calc::hyperbolic_inverse::asinh;
/// let result = asinh(1.0);
/// assert_eq!(result, 1.0_f64.asinh());
/// ```
pub fn asinh(a: f64) -> f64 {
    a.asinh()
}

