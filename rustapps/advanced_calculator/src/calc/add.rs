/// Adds two integers and returns the result.
///
/// # Arguments
///
/// * `a` - An integer to be added.
/// * `b` - Another integer to be added.
///
/// # Returns
///
/// The sum of `a` and `b`.
///
/// # Examples
///
/// ```
/// use advanced_calculator::calc::add::add;
/// let result = add(2, 3);
/// assert_eq!(result, 5);
/// ```
pub fn add(a: i32, b: i32) -> i32 {
    a + b
}
